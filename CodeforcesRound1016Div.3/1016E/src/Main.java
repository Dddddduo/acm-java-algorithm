    /**
     * @throws IOException
     */
    public static void solve() throws IOException {
        int n = sc.nextInt();
        int k = sc.nextInt();
        int[] a = new int[n];

        for (int i = 0; i < n; i++) {
            a[i] = sc.nextInt();
        }

        int left = 0, right = n + 1;
        while (left < right) {

            int mid = left + (right - left + 1) / 2;

//            int mid=(left+right)/2;

            if (judge(a, k, mid)) {
                left = mid;
            } else {
                right = mid - 1;
            }
        }

        dduoln(left);
    }

    private static boolean judge(int[] a, int k, int target) {
        int count = 0;

        boolean[] visited = new boolean[target];
        int m = 0;

        for (int num : a) {
            if (num < target) {
                visited[num] = true;
            }
            while (m < target && visited[m]) {
                m++;
            }
            if (m == target) {
                count++;
                visited = new boolean[target];
                m = 0;
            }
        }
        return count >= k;
    }

    public static void main(String[] args) throws Exception {
        int t = 1;
        t = sc.nextInt();
        while (t-- > 0) {
            solve();
        }
    }

